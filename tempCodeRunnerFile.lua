def longest_path(matrix):
    num_rows = len(matrix)
    num_cols = len(matrix[0])
    visited = [[False] * num_cols for _ in range(num_rows)]

    def dfs(row, col, path):
        if row == num_rows - 1 and col == num_cols - 1:
            return len(path)

        max_length = 0
        directions = [(0, 1), (0, -1), (1, 0), (-1, 0)]
        for dx, dy in directions:
            new_row, new_col = row + dx, col + dy
            if is_valid_move(new_row, new_col, visited, matrix):
                visited[new_row][new_col] = True
                length = dfs(new_row, new_col, path + [(new_row, new_col)])
                max_length = max(max_length, length)
                visited[new_row][new_col] = False
        return max_length

    def is_valid_move(row, col, visited, matrix):
        if not (0 <= row < num_rows and 0 <= col < num_cols):
            return False
        if visited[row][col]:
            return False
        if matrix[row][col] == 1:
            return False
        return True

    start_row, start_col = 0, 0
    start_path = [(start_row, start_col)]
    visited[start_row][start_col] = True

    return dfs(start_row, start_col, start_path)




