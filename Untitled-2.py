    num_cols = len(matrix[0])
    visited = [[False] * num_cols for _ in range(num_rows)]

    def dfs(row, col, path):
        if row == num_rows - 1 and col == num_cols - 1:
            return path

        longest_path = []
        directions = [(0, 1), (0, -1), (1, 0), (-1, 0)]
        for dx, dy in directions:
            new_row, new_col = row + dx, col + dy
            if is_valid_move(new_row, new_col, visited, matrix):
                visited[new_row][new_col] = True
                new_path = dfs(new_row, new_col, path + [(new_row, new_col)])
                if len(new_path) > len(longest_path):
                    longest_path = new_path
                visited[new_row][new_col] = False
        return longest_path

    def is_valid_move(row, col, visited, matrix):
        if not (0 <= row < num_rows and 0 <= col < num_cols):
            return False
        if visited[row][col]:
            return False
        if matrix[row][col] == 1:
            return False
        return True

    start_row, start_col = 0, 0
    start_path = [(start_row, start_col)]
    visited[start_row][start_col] = True

    longest_path_coords = dfs(start_row, start_col, start_path)
    if not longest_path_coords:
        return "No valid path exists."

    # Create an empty grid to hold the path
    path_grid = [["."] * num_cols for _ in range(num_rows)]
    for row, col in longest_path_coords:
        path_grid[row][col] = "#"

    # Format the grid as ASCII art
